/// <summary>
/// Represents an ASCII Letter.
/// </summary>
internal class AsciiLetter
{
    /// <summary>
    /// Represents the character of the letter.
    /// </summary>
    public string? Letter { get; set; }

    /// <summary>
    /// Represents the table of string representing an ASCII letter.
    /// </summary>
    public string[]? AsciiLetterBits { get; set; }
}